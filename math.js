 // Double number
function double(n) {
  return n * 2;
}

// Square number
function square(n) {
  return n * n;
}

// Check if even
function isEven(n) {
  return n % 2 === 0;
}

// Check if odd
function isOdd(n) {
  return n % 2 !== 0;
}

// Multiply numbers
function multiply(a, b) {
  return a * b;
}
